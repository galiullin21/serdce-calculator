// KeyTo numerology calculation system

export interface NumerologyResult {
  mindNumber: number;      // Число Ума - день рождения
  actionNumber: number;    // Число Действия - месяц  
  realizationNumber: number; // Число Реализации - год
  totalNumber: number;     // Число Итога - сумма всех
  birthDate: Date;
}

// Сводим число к однозначному (1-9)
export function reduceToSingleDigit(num: number): number {
  while (num > 9) {
    num = String(num).split('').reduce((sum, digit) => sum + parseInt(digit), 0);
  }
  return num;
}

// Главный расчет нумерологии
export function calculateNumerology(day: number, month: number, year: number): NumerologyResult {
  // Число Ума = день рождения (приведенный к 1-9)
  const mindNumber = reduceToSingleDigit(day);
  
  // Число Действия = месяц (приведенный к 1-9)
  const actionNumber = reduceToSingleDigit(month);
  
  // Число Реализации = год (приведенный к 1-9)
  const realizationNumber = reduceToSingleDigit(year);
  
  // Число Итога = сумма всех чисел даты
  const totalSum = day + month + year;
  const totalNumber = reduceToSingleDigit(totalSum);
  
  return {
    mindNumber,
    actionNumber,
    realizationNumber,
    totalNumber,
    birthDate: new Date(year, month - 1, day)
  };
}

